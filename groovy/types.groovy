// Boolean
b = true;
println b;

// Integer
n = 1;
println n;

// Float
x = 3.14;
println x;

// String
s = "Hello";
println s;

// List
l = [1, 2, 3];
println l;

// Map
d = ["one":"un", "two":"deux", "three":"trois"];
println d;
